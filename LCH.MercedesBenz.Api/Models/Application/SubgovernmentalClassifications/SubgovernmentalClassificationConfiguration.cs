using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Metadata;
using Microsoft.EntityFrameworkCore.Metadata.Builders;

namespace LCH.MercedesBenz.Api.Models.Application.SubgovernmentalClassifications
{
    public class SubgovernmentalClassificationConfiguration : IEntityTypeConfiguration<SubgovernmentalClassification>
    {
        public void Configure(EntityTypeBuilder<SubgovernmentalClassification> builder)
        {
            builder
                .Property(b => b.AutoId)
                .UseIdentityColumn();

            builder
                .Property(b => b.AutoId)
                .Metadata.SetAfterSaveBehavior(PropertySaveBehavior.Ignore);
        }
    }
}
