using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Metadata;
using Microsoft.EntityFrameworkCore.Metadata.Builders;

namespace LCH.MercedesBenz.Api.Models.Application.GovernmentalClassifications
{
    public class GovernmentalClassificationConfiguration : IEntityTypeConfiguration<GovernmentalClassification>
    {
        public void Configure(EntityTypeBuilder<GovernmentalClassification> builder)
        {
            builder
                .Property(b => b.AutoId)
                .UseIdentityColumn();

            builder
                .Property(b => b.AutoId)
                .Metadata.SetAfterSaveBehavior(PropertySaveBehavior.Ignore);
        }
    }
}
